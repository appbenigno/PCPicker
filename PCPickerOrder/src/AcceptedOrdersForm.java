    private Order getOrderFromList(int orderid)
    {
        for(Order order : orderList)
            if(order.getOrderId() == orderid)
                return order;
        return null;
    }
    private Customer getCustomerFromList(int custId)
    {
        for(Customer customer : customerList)
            if(customer.getCustId()== custId)
                return customer;
        return null;
    }
    
    private void updateAll()
    {
        orderList = WebMethods.getActivePendingOrderList();
        customerList = WebMethods.getCustomerList_1();
        System.out.println("customerList.size: " + customerList.size());
       
        populateTable();
       
    }

    public void populateTable(){
        DefaultTableModel tblModel = (DefaultTableModel)tblOrders.getModel();
        tblModel.setRowCount(0);
        Object row[] = new Object[5];
        for (int i = 0 ; i < orderList.size() ; i++){
            row[0] = orderList.get(i).getOrderId();
            row[1] = orderList.get(i).getCustId();
            row[2] = "â‚± " + Double.toString(calculateTotal(orderList.get(i)));
            row[3] = orderList.get(i).getDateCreated();
            row[4] = orderList.get(i).getDeliveryAddress();
            tblModel.addRow(row);            
        }
    }
    
    private double calculateTotal(Order order)
    {
        double total = 0;
        for(OrderParts op : order.getItems())        
            total += op.getPrice() * op.getQuantity();
        return total;    
    }
    private void addListeners()
    {
        tblOrders.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
            public void valueChanged(ListSelectionEvent event) {

                int selectedRow = tblOrders.getSelectedRow();
                if(selectedRow >=0)
                {
                    int custId = Integer.parseInt(tblOrders.getValueAt(selectedRow, 1).toString());
                    int orderId = Integer.parseInt(tblOrders.getValueAt(selectedRow, 0).toString());
                    viewCustomerDetails(custId);
                    viewOrderDetails(orderId);
                    btnAcceptOrder.setEnabled(true);
                    btnRejectOrder.setEnabled(true);
                }

            }
        });
    }
    private void viewOrderDetails(int orderId)
    {
        lblPaymentType.setVisible(true);
        lblNumItems.setVisible(true);
        Order order = getOrderFromList(orderId);
        lblPaymentType.setText(order.getPaymentType());
        int numItems=0;
        for(OrderParts op : order.getItems())
            numItems += op.getQuantity();
        lblNumItems.setText(Integer.toString(numItems));
    }
    private void viewCustomerDetails(int custId)
    {
        lblFName.setVisible(true);
        lblLName.setVisible(true);
        lblEmail.setVisible(true);        
        Customer customer = getCustomerFromList(custId);
        lblFName.setText(customer.getFirstname());
        lblLName.setText(customer.getLastname());
        lblEmail.setText(customer.getUsername());
        
    }
